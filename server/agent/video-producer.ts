// ─── Script Generation for Modules 5-8 ───────────────────────────────────

/**
 * Generate a video script for a lesson that doesn't have one written yet.
 * Uses the LLM with brand context and the existing script style.
 */
async function generateVideoScript(moduleNumber: number, lessonNumber: number, lessonTitle: string): Promise<string> {
  const prompt = `You are writing a video script for Shaun Critzer's "From Broken to Whole" 30-day recovery course.

Shaun is a recovery coach, former Mr. Teen USA, and author of the memoir "Crooked Lines: Bent, Not Broken." He's been sober 13+ years. His style is:
- Direct and vulnerable — shares his own story openly
- Trauma-informed — addresses root causes, not just symptoms
- Neuroscience-backed — references research and brain science
- Empathetic but firm — not preachy, not enabling
- Conversational — talks TO the viewer, not AT them

This is Module ${moduleNumber}, Lesson ${lessonNumber}: "${lessonTitle}"

Write a 12-15 minute video script (approximately 2000-2500 characters) in Shaun's voice. It should:
1. Open with a personal connection or story
2. Teach the core concept for this lesson
3. Reference relevant psychology/neuroscience
4. Include a practical exercise or takeaway
5. Close with encouragement and a reference to "your workbook for today"

Write ONLY the spoken words (no stage directions, no formatting). Write as if Shaun is speaking directly to camera.`;

  try {
    const result = await invokeLLM({
      messages: [
        { role: "system", content: "You are a skilled scriptwriter for trauma-informed recovery content. Write natural, conversational scripts." },
        { role: "user", content: prompt },
      ],
    });

    const rawContent = result?.choices?.[0]?.message?.content;
    const content = typeof rawContent === "string" ? rawContent : Array.isArray(rawContent) ? rawContent.map((c: any) => c.text || "").join("") : "";
    return content || `Welcome to Day ${(moduleNumber - 1) * 4 + lessonNumber} of From Broken to Whole. Today we're going to talk about ${lessonTitle}. Open your workbook and let's get started.`;
  } catch {
    return `Welcome to Day ${(moduleNumber - 1) * 4 + lessonNumber} of From Broken to Whole. Today we're going to talk about ${lessonTitle}. Open your workbook and let's get started.`;
  }
}

// ─── Single Video Generation ──────────────────────────────────────────────

/**
 * Generate a single course video: script → HeyGen → storage → database
 */
export async function generateCourseVideo(opts: {
  lessonId: number;
  script: string;
  title: string;
  moduleNumber: number;
  lessonNumber: number;
  test?: boolean; // HeyGen test mode (watermarked, doesn't use credits)
}): Promise<VideoJob> {
  const { createVideo, waitForVideo, downloadVideo, isHeyGenConfigured } = await import("../social/heygen");

  const job: VideoJob = {
    lessonId: opts.lessonId,
    moduleTitle: `Module ${opts.moduleNumber}`,
    lessonTitle: opts.title,
    script: opts.script.substring(0, 100) + "...",
    status: "queued",
  };

  if (!isHeyGenConfigured()) {
    job.status = "failed";
    job.error = "HeyGen API key not configured";
    return job;
  }

  try {
    // Extract clean spoken text from script
    const spokenText = extractSpokenText(opts.script);

    job.status = "generating";

    // Create video with Shaun's avatar and voice
    const createResult = await createVideo({
      script: spokenText,
      avatarId: process.env.HEYGEN_AVATAR_ID,
      voiceId: process.env.ELEVENLABS_VOICE_ID, // Shaun's cloned voice
      title: `${opts.title} - From Broken to Whole`,
      aspectRatio: "16:9",
      test: opts.test,
    });

    if (!createResult.success || !createResult.videoId) {
      job.status = "failed";
      job.error = createResult.error || "Failed to create video";
      return job;
    }

    job.heygenVideoId = createResult.videoId;

    // Wait for HeyGen to finish rendering (polls every 15s, max 10min)
    const status = await waitForVideo(createResult.videoId);

    if (status.status !== "completed" || !status.videoUrl) {
      job.status = "failed";
      job.error = status.error || "Video generation timed out";
      return job;
    }

    job.videoUrl = status.videoUrl;
    job.duration = status.duration;

    // Update the database with the video URL
    const db = await getDb();
    if (db) {
      const { courseLessons } = await import("../../drizzle/schema");
      const { eq } = await import("drizzle-orm");

      await db.update(courseLessons)
        .set({
          videoUrl: status.videoUrl,
          videoProvider: "other",
          videoDuration: status.duration || null,
        })
        .where(eq(courseLessons.id, opts.lessonId));
    }

    // Log action
    await logVideoAction({
      action: "generate_course_video",
      lessonId: opts.lessonId,
      title: opts.title,
      videoUrl: status.videoUrl,
      duration: status.duration,
      riskTier: 2,
    });

    job.status = "completed";
    return job;
  } catch (err: any) {
    job.status = "failed";
    job.error = err.message;
    return job;
  }
}

// ─── Batch Video Generation ───────────────────────────────────────────────

/**
 * Generate videos for all lessons in the From Broken to Whole course.
 * Processes one at a time to respect HeyGen rate limits.
 *
 * Options:
 *   - moduleFilter: Only generate for specific module numbers
 *   - skipExisting: Skip lessons that already have video URLs
 *   - test: Use HeyGen test mode (watermarked, free)
 *   - maxVideos: Limit number of videos to generate
 */
export async function batchGenerateCourseVideos(opts?: {
  moduleFilter?: number[];
  skipExisting?: boolean;
  test?: boolean;
  maxVideos?: number;
}): Promise<BatchResult> {
  const db = await getDb();
  if (!db) return { success: false, totalJobs: 0, completed: 0, failed: 0, jobs: [], error: "Database not available" };

  const { courseModules, courseLessons } = await import("../../drizzle/schema");
  const { eq, and, isNull } = await import("drizzle-orm");

  // Get all modules for the course
  const modules = await db.select().from(courseModules)
    .where(eq(courseModules.productId, "from-broken-to-whole"))
    .orderBy(courseModules.sortOrder);

  if (modules.length === 0) {
    return { success: false, totalJobs: 0, completed: 0, failed: 0, jobs: [], error: "Course not seeded. Run 'Seed 30-Day Course' first." };
  }

  const jobs: VideoJob[] = [];
  let videosGenerated = 0;
  const maxVideos = opts?.maxVideos || 999;

  for (const mod of modules) {
    // Apply module filter
    if (opts?.moduleFilter && !opts.moduleFilter.includes(mod.moduleNumber)) continue;

    // Get lessons for this module
    const lessons = await db.select().from(courseLessons)
      .where(eq(courseLessons.moduleId, mod.id))
      .orderBy(courseLessons.sortOrder);

    for (const lesson of lessons) {
      if (videosGenerated >= maxVideos) break;

      // Skip lessons with existing videos if requested
      if (opts?.skipExisting && lesson.videoUrl) continue;

      // Get or generate the script
      const scriptInfo = VIDEO_SCRIPT_MAP[mod.moduleNumber]?.[lesson.lessonNumber];
      let script: string;

      if (scriptInfo?.available) {
        // Script exists in the markdown files — the content will be loaded
        // by the caller or we generate a condensed version
        script = await generateVideoScript(mod.moduleNumber, lesson.lessonNumber, lesson.title);
      } else {
        // Generate script via LLM
        script = await generateVideoScript(mod.moduleNumber, lesson.lessonNumber, lesson.title);
      }

      // Generate the video
      console.log(`[VideoProducer] Generating video for Module ${mod.moduleNumber}, Lesson ${lesson.lessonNumber}: ${lesson.title}`);

      const job = await generateCourseVideo({
        lessonId: lesson.id,
        script,
        title: lesson.title,
        moduleNumber: mod.moduleNumber,
        lessonNumber: lesson.lessonNumber,
        test: opts?.test,
      });

      jobs.push(job);
      if (job.status === "completed") videosGenerated++;
    }
  }

  const completed = jobs.filter(j => j.status === "completed").length;
  const failed = jobs.filter(j => j.status === "failed").length;

  return {
    success: failed === 0 || completed > 0,
    totalJobs: jobs.length,
    completed,
    failed,
    jobs,
  };
}

// ─── Audio Generation (ElevenLabs) ────────────────────────────────────────

/**
 * Generate audio narration for a course lesson using ElevenLabs voice clone.
 * Alternative to HeyGen when you want audio-only (podcast-style lessons).
 */
export async function generateLessonAudio(opts: {
  lessonId: number;
  script: string;
  title: string;
}): Promise<{ success: boolean; audioUrl?: string; error?: string }> {
  try {
    const { textToSpeech } = await import("../social/elevenlabs");

    const spokenText = extractSpokenText(opts.script);

    const result = await textToSpeech({
      text: spokenText,
      voiceId: process.env.ELEVENLABS_VOICE_ID,
    });

    if (!result.success || !result.audioBuffer) {
      return { success: false, error: result.error || "Audio generation failed" };
    }

    // Upload audio to storage
    try {
      const { storagePut } = await import("../storage");
      const key = `courses/from-broken-to-whole/audio/${opts.title.replace(/[^a-zA-Z0-9]/g, "_")}.mp3`;
      const { url } = await storagePut(key, result.audioBuffer, "audio/mpeg");

      // Update database
      const db = await getDb();
      if (db) {
        const { courseLessons } = await import("../../drizzle/schema");
        const { eq } = await import("drizzle-orm");
        // Store audio URL in the slide PDF field as a workaround
        // (no dedicated audio column in schema)
        await db.update(courseLessons)
          .set({ slidePdfUrl: url })
          .where(eq(courseLessons.id, opts.lessonId));
      }

      return { success: true, audioUrl: url };
    } catch {
      // Storage not available — return the raw buffer status
      return { success: true, audioUrl: "generated-but-storage-unavailable" };
    }
  } catch (err: any) {
    return { success: false, error: err.message };
  }
}

// ─── Status Check ─────────────────────────────────────────────────────────

/**
 * Check HeyGen account status: credits remaining, avatars available
 */
export async function checkVideoCapability(): Promise<{
  heygenConfigured: boolean;
  elevenlabsConfigured: boolean;
  heygenCredits?: { remaining: number; used: number };
  avatarId?: string;
  voiceId?: string;
  error?: string;
}> {
  const heygenConfigured = !!process.env.HEYGEN_API_KEY;
  const elevenlabsConfigured = !!process.env.ELEVENLABS_API_KEY;

  const result: any = {
    heygenConfigured,
    elevenlabsConfigured,
    avatarId: process.env.HEYGEN_AVATAR_ID || "not set (will auto-detect)",
    voiceId: process.env.ELEVENLABS_VOICE_ID || "not set (will use default)",
  };

  if (heygenConfigured) {
    try {
      const { getRemainingCredits } = await import("../social/heygen");
      const credits = await getRemainingCredits();
      if (credits) {
        result.heygenCredits = { remaining: credits.remainingQuota, used: credits.usedQuota };
      }
    } catch (err: any) {
      result.error = `HeyGen credit check failed: ${err.message}`;
    }
  }

  return result;
}

// ─── Action Logging ───────────────────────────────────────────────────────

async function logVideoAction(opts: {
  action: string;
  lessonId: number;
  title: string;
  videoUrl?: string;
  duration?: number;
  riskTier: number;
}) {
  try {
    const db = await getDb();
    if (!db) return;

    const { agentActions } = await import("../../drizzle/schema");

    await db.insert(agentActions).values({
      category: "content",
      title: `Video: ${opts.title}`,
      description: `Generated video for course lesson: ${opts.title}`,
      riskTier: opts.riskTier,
      status: "executed",
      executedAt: new Date(),
      metadata: JSON.stringify({
        lessonId: opts.lessonId,
        videoUrl: opts.videoUrl,
        duration: opts.duration,
        agent: "video-producer",
        action: opts.action,
      }),
    });
  } catch {
    // Non-critical — don't fail video generation if logging fails
  }
}
