/**
 * VIDEO PRODUCER AGENT - Course Video Generation Pipeline
 *
 * The first specialized "arm" of Mission Control. Takes course lesson scripts
 * and produces HeyGen avatar videos with ElevenLabs voice cloning.
 *
 * Pipeline:
 *   1. Read video scripts from course content
 *   2. Generate HeyGen avatar videos (using Shaun's avatar + cloned voice)
 *   3. Poll for completion
 *   4. Download completed videos
 *   5. Upload to storage (Forge proxy / R2)
 *   6. Update courseLessons.videoUrl in database
 *   7. Log all actions to agent_actions for audit trail
 *
 * Risk Tier: 2 (auto-execute + notify — video generation costs credits but < $25)
 *
 * HeyGen limit: scripts must be <= 3,000 characters
 */

import { getDb } from "../db";
import { invokeLLM } from "../_core/llm";

// ─── Types ─────────────────────────────────────────────────────────────────

export type VideoJob = {
  lessonId: number;
  moduleTitle: string;
  lessonTitle: string;
  script: string;
  status: "queued" | "generating" | "completed" | "failed";
  heygenVideoId?: string;
  videoUrl?: string;
  error?: string;
  duration?: number;
};

export type BatchResult = {
  success: boolean;
  totalJobs: number;
  completed: number;
  failed: number;
  jobs: VideoJob[];
  error?: string;
};

// ─── Script Extraction ────────────────────────────────────────────────────

/**
 * Extract the spoken script text from a video script markdown section.
 * Strips stage directions, scene descriptions, and formatting.
 * Ensures output is <= 3000 chars (HeyGen limit).
 */
function extractSpokenText(rawScript: string): string {
  let text = rawScript;

  // Remove stage directions like **(Scene: ...)**
  text = text.replace(/\*\*\(.*?\)\*\*/g, "");
  // Remove bold speaker labels like **Shaun:**
  text = text.replace(/\*\*Shaun:\*\*/g, "");
  // Remove markdown bold/italic markers
  text = text.replace(/\*\*([^*]+)\*\*/g, "$1");
  text = text.replace(/\*([^*]+)\*/g, "$1");
  // Remove markdown headers
  text = text.replace(/^#{1,4}\s+.*$/gm, "");
  // Remove horizontal rules
  text = text.replace(/^---+$/gm, "");
  // Clean up excessive whitespace
  text = text.replace(/\n{3,}/g, "\n\n").trim();

  // HeyGen has a 3000 char limit — truncate at sentence boundary if needed
  if (text.length > 3000) {
    const truncated = text.substring(0, 2900);
    const lastSentence = truncated.lastIndexOf(".");
    if (lastSentence > 2000) {
      text = truncated.substring(0, lastSentence + 1);
    } else {
      text = truncated + "...";
    }
  }

  return text;
}

